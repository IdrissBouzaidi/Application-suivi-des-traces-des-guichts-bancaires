package com.idriss.demo.classes;

public class Message {
	String[][] message;

	
	public Message() {
		super();
	}


	public Message(String[][] message) {
		super();
		this.message = message;
	}


	public String[][] getMessage() {
		return message;
	}


	public void setMessage(String[][] message) {
		this.message = message;
	}

	
}
